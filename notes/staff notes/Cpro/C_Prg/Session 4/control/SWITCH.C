
/*SWITCH CASE:
~~~~~~~~~~~~~
	1. The switch statement matches the expression value to the
case label.
	2. If a match found then the statement followed by the case
label is executed.
	3. if there is no match found,then the statement in the default
case is executed.
	4.case label must be unique and must be integer (or) character.

syntax:
~~~~~~~
 switch(expression)
 {
  case label1:
    statement 1;
    break;
  case label2:
    statement 2;
    break;
  .
  .
  .
  .
  case labeln:
    statement n;
    break;
  default:
    default statements;
}*/
